import UIKit

extension UIColor{
    class func studyBox_darkBlue() -> UIColor{
        return UIColor(red: 0, green: 104, blue: 180, alpha: 1)
    }
    class func studyBox_raspberry() -> UIColor{
        return UIColor(red: 234, green: 0, blue: 97, alpha: 1)
    }
    class func studyBox_graphite() -> UIColor{
        return UIColor(red: 66, green: 66, blue: 66, alpha: 1)
    }
    class func studyBox_grey() -> UIColor{
        return UIColor(red: 250, green: 250, blue: 250, alpha: 1)
    }
}

